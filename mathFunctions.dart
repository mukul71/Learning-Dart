// Dart has a long range of math function. To use dart's math functions
// we need to import dart:math library first.
//After importing we can use as below examples.

import 'dart:math';

void main (){
  print(sin(45 * pi / 180));  //output: 0.7071067811865476
  print(cos(135 * pi / 180)); //output: -0.7071067811865475
  print(sqrt(2));             //output: 1.4142135623730951
  print(max(5,10));
  print(min(-5,-10));
}
