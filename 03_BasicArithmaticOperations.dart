void main(){
  // In this file we will see most basic 4 operators used in Dart
  // They are, Addition (+), Subtraction (-), Multiplication(*) & division(/)
  int a = 19;
  int b = 2;

  // Addition

  print("The result of ${a}+${b} is: ${a+b}");
  //Subtraction
  print("The result of ${a}-${b} is:${a-b}");
  //Multiplication
  print("The result of ${a}x${b} is:${a*b}");
  ///Division
  print("The result of ${a}/${b} is:${a/b}");

}
